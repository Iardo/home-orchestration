# Refreshing a Look
# There may be times that the UI of the system and the Regolith look get out of sync.
# For example, if a program sets the wallpaper or icon theme externally,
# this will not be reflected in the Xresources settings.
#
# Regolith will only update itself upon login if it detects of Xresources changes
# have been made. If you wish to force Regolith to reload the UI based on the
# Xresources settings, run:
#   $ regolith-look refresh
#
# https://regolith-linux.org/docs/customize/look/#refreshing-a-look

regolith-look refresh
